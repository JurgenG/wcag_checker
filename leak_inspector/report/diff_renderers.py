"""Format-specific renderers for :class:`ReportDiff`.

One function per format (HTML, Markdown, Text, JSON); all consume the
same :class:`ReportDiff`. They reuse the per-site reporters' chrome
(BeLibre header, "About this report" intro, capture-status banner) so
the visual identity stays consistent across single-site and diff
reports.
"""

from __future__ import annotations

import json
from dataclasses import asdict
from html import escape as _h
from io import StringIO

from ._branding import (
    BELIBRE_HOMEPAGE,
    belibre_logo_svg_inline,
    BRANDING_TITLE_PREFIX,
)
from .diff import ReportDiff
from .html import (
    _CSS,
    _render_capture_status_banner,
    _render_intro,
)


# --- JSON ------------------------------------------------------------------


def render_diff_json(diff: ReportDiff) -> str:
    """Render a :class:`ReportDiff` as a JSON string.

    Uses :func:`dataclasses.asdict` so every nested dataclass round-trips
    cleanly. ``capture_status_*`` may be ``None`` on the diff; the
    encoder handles that without special-casing.
    """
    return json.dumps(asdict(diff), indent=2, ensure_ascii=False, default=str)


# --- HTML ------------------------------------------------------------------


def render_diff_html(
    diff: ReportDiff,
    *,
    side_report_a: str | None = None,
    side_report_b: str | None = None,
) -> str:
    """Render a :class:`ReportDiff` as a standalone HTML page.

    Reuses the per-site reporters' chrome (BeLibre branding, intro
    disclaimer, capture-status banner CSS) so the visual identity stays
    consistent across single-site and diff reports.

    ``side_report_a`` / ``side_report_b`` are optional relative paths
    to per-side reports written as sibling files (the CLI's ``--out``
    mode produces them). When set, the diff renders a small "Full
    report" link block near the top so the auditor can jump from the
    delta view into the full single-site analysis for either side.
    """
    out = StringIO()
    out.write("<!doctype html>\n")
    out.write('<html lang="en">\n<head>\n<meta charset="utf-8">\n')
    host_label = diff.manifest_b.base_domain or diff.manifest_a.base_domain or "(diff)"
    out.write(
        f"<title>{_h(BRANDING_TITLE_PREFIX)} — Diff: {_h(host_label)} "
        f"({_h(diff.label_a)} → {_h(diff.label_b)})</title>\n"
    )
    out.write(f"<style>{_CSS}{_DIFF_CSS}</style>\n</head>\n<body>\n")

    _render_diff_header(out, diff, host_label)
    _render_intro(out)
    _render_bundle_mismatch(out, diff)
    _render_capture_status_banner(out, diff.capture_status_a)
    _render_capture_status_banner(out, diff.capture_status_b)
    _render_side_report_links_html(
        out, diff, side_report_a, side_report_b,
    )

    _render_diff_headline(out, diff)
    _render_personal_data_delta(out, diff)
    _render_modules_only_in_b(out, diff)
    _render_modules_changed(out, diff)
    _render_modules_only_in_a(out, diff)
    _render_cookie_delta(out, diff)
    _render_storage_delta(out, diff)
    _render_host_delta(out, diff)
    _render_finding_delta(out, diff)
    _render_new_jurisdictions(out, diff)

    out.write(
        "<footer>Generated by leak_inspector — diff "
        f"<code>{_h(diff.label_a)}</code> → <code>{_h(diff.label_b)}</code>"
        f" · session {_h(diff.manifest_a.session_id)} → "
        f"{_h(diff.manifest_b.session_id)}</footer>\n"
    )
    out.write("</body>\n</html>\n")
    return out.getvalue()


