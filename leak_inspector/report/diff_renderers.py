def render_diff_text(
    diff: ReportDiff,
    *,
    color: bool = True,
    side_report_a: str | None = None,
    side_report_b: str | None = None,
) -> str:
    """Render a :class:`ReportDiff` as plain text for terminal display.

    Reuses the existing text reporter's palette for bold / section
    headers / impact colours so the look matches single-site reports.
    Set ``color=False`` to suppress all ANSI escapes (useful for
    piping into less / grep, and for tests).

    ``side_report_a`` / ``side_report_b`` are optional relative paths
    surfaced near the top so the operator knows where to find each
    side's full single-site report.
    """
    from .text import _Palette, _write_capture_status_banner, _write_intro

    p = _Palette(color)
    out = StringIO()
    host_label = diff.manifest_b.base_domain or diff.manifest_a.base_domain or "(diff)"

    bar = "=" * 78
    out.write(f"{bar}\n")
    out.write(p.header(f"{BRANDING_TITLE_PREFIX} — Diff: {host_label}") + "\n")
    out.write(p.dim(f"comparing:  {diff.label_a}  →  {diff.label_b}") + "\n")
    out.write(p.dim(f"sessions:   {diff.manifest_a.session_id}  →  "
                    f"{diff.manifest_b.session_id}") + "\n")
    out.write(f"{bar}\n\n")

    _write_intro(out, p)
    if diff.bundle_mismatch:
        out.write(p.impact("high", f"⚠ Bundle mismatch: {diff.bundle_mismatch}") + "\n\n")
    _write_capture_status_banner(out, diff.capture_status_a, p)
    _write_capture_status_banner(out, diff.capture_status_b, p)

    if side_report_a or side_report_b:
        out.write(p.header("Full per-side reports:") + "\n")
        if side_report_a:
            out.write(f"  {diff.label_a}: {side_report_a}\n")
        if side_report_b:
            out.write(f"  {diff.label_b}: {side_report_b}\n")
        out.write("\n")

    out.write(p.header(diff.headline) + "\n")
    out.write("─" * 78 + "\n\n")

    pdd = diff.personal_data_delta
    if pdd.only_in_a or pdd.only_in_b:
        out.write(p.section(
            f"Personal-data field delta "
            f"({diff.label_a} {pdd.count_a} → {diff.label_b} {pdd.count_b})"
        ) + "\n")
        out.write("─" * 78 + "\n")
        if pdd.only_in_b:
            out.write(p.dim(f"  New in {diff.label_b}:") + "\n")
            for f in pdd.only_in_b:
                out.write(f"    + {f.vendor}  ({f.category})  {f.key}\n")
        if pdd.only_in_a:
            out.write(p.dim(f"  Only in {diff.label_a}:") + "\n")
            for f in pdd.only_in_a:
                out.write(f"    - {f.vendor}  ({f.category})  {f.key}\n")
        out.write("\n")

    if diff.modules_only_in_b:
        out.write(p.section(f"New trackers in {diff.label_b}") + "\n")
        out.write("─" * 78 + "\n")
        for m in diff.modules_only_in_b:
            vendor = m.vendor_meta.vendor or ""
            juris = m.vendor_meta.legal_jurisdiction or ""
            extras = " · ".join([x for x in (vendor, juris) if x])
            tail = f"  ({extras})" if extras else ""
            out.write(f"  + {m.module_name}{tail}\n")
        out.write("\n")

    if diff.modules_changed:
        out.write(p.section("Trackers with field-set changes") + "\n")
        out.write("─" * 78 + "\n")
        for c in diff.modules_changed:
            vendor = f"  ({c.vendor})" if c.vendor else ""
            out.write(f"  ~ {c.module_name}{vendor}\n")
            if c.fields_added:
                out.write(p.dim(f"      added:   +{', '.join(c.fields_added)}") + "\n")
            if c.fields_removed:
                out.write(p.dim(f"      removed: -{', '.join(c.fields_removed)}") + "\n")
            if c.hit_count_a != c.hit_count_b:
                out.write(p.dim(f"      hits:    {c.hit_count_a} -> {c.hit_count_b}") + "\n")
        out.write("\n")

    if diff.modules_only_in_a:
        out.write(p.section(f"Trackers no longer firing in {diff.label_b}") + "\n")
        out.write("─" * 78 + "\n")
        for m in diff.modules_only_in_a:
            vendor = f"  ({m.vendor_meta.vendor})" if m.vendor_meta.vendor else ""
            out.write(f"  - {m.module_name}{vendor}\n")
        out.write("\n")

    if diff.cookies_added or diff.cookies_removed:
        out.write(p.section("Cookie delta") + "\n")
        out.write("─" * 78 + "\n")
        if diff.cookies_added:
            out.write(p.dim(f"  New in {diff.label_b}:") + "\n")
            for c in diff.cookies_added:
                party = "1P" if c.is_first_party else "3P"
                out.write(
                    f"    + {c.name:<28} @ {c.host}  "
                    f"[{party}, {c.lifetime_human}]\n"
                )
        if diff.cookies_removed:
            out.write(p.dim(f"  Only in {diff.label_a}:") + "\n")
            for c in diff.cookies_removed:
                party = "1P" if c.is_first_party else "3P"
                out.write(
                    f"    - {c.name:<28} @ {c.host}  "
                    f"[{party}, {c.lifetime_human}]\n"
                )
        out.write("\n")

    if diff.storage_added or diff.storage_removed:
        out.write(p.section("Browser-storage delta") + "\n")
        out.write("─" * 78 + "\n")
        if diff.storage_added:
            out.write(p.dim(f"  New in {diff.label_b}:") + "\n")
            for s in diff.storage_added:
                out.write(f"    + [{s.kind}] {s.key}  @ {s.origin}\n")
        if diff.storage_removed:
            out.write(p.dim(f"  Only in {diff.label_a}:") + "\n")
            for s in diff.storage_removed:
                out.write(f"    - [{s.kind}] {s.key}  @ {s.origin}\n")
        out.write("\n")

    if diff.hosts_only_in_a or diff.hosts_only_in_b:
        out.write(p.section("Third-party host delta") + "\n")
        out.write("─" * 78 + "\n")
        if diff.hosts_only_in_b:
            out.write(p.dim(f"  Only in {diff.label_b}:") + "\n")
            for h in diff.hosts_only_in_b:
                out.write(f"    + {h}\n")
        if diff.hosts_only_in_a:
            out.write(p.dim(f"  Only in {diff.label_a}:") + "\n")
            for h in diff.hosts_only_in_a:
                out.write(f"    - {h}\n")
        out.write("\n")

    if diff.findings_only_in_a or diff.findings_only_in_b:
        out.write(p.section("Finding delta") + "\n")
        out.write("─" * 78 + "\n")
        for f in diff.findings_only_in_b:
            detail = f"  — {f.detail}" if f.detail else ""
            out.write(f"  + {f.badge} {f.headline}{detail}\n")
        for f in diff.findings_only_in_a:
            detail = f"  — {f.detail}" if f.detail else ""
            out.write(f"  - {f.badge} {f.headline}{detail}\n")
        out.write("\n")

    if diff.new_jurisdictions:
        out.write(p.section(f"New jurisdictions in {diff.label_b}") + "\n")
        out.write("─" * 78 + "\n")
        out.write("  " + ", ".join(diff.new_jurisdictions) + "\n\n")

    return out.getvalue()


