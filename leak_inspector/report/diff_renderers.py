def _render_diff_header(out: StringIO, diff: ReportDiff, host_label: str) -> None:
    home = _h(BELIBRE_HOMEPAGE)
    out.write('<header class="diff-header">\n')
    out.write('<h1 class="report-title">\n')
    out.write(
        f'<a class="belibre-link" href="{home}" target="_blank" rel="noopener">'
        f"{belibre_logo_svg_inline()}"
        f"</a>\n"
    )
    out.write(
        f"<span>{_h(BRANDING_TITLE_PREFIX)} — Diff: </span>"
        f"<span>{_h(host_label)}</span>"
    )
    out.write("</h1>\n")
    out.write(
        f'<div class="diff-labels">'
        f"comparing <code>{_h(diff.label_a)}</code>"
        f" → <code>{_h(diff.label_b)}</code>"
        f"</div>\n"
    )
    out.write("</header>\n")


def _render_diff_headline(out: StringIO, diff: ReportDiff) -> None:
    out.write(f'<div class="diff-headline">{_h(diff.headline)}</div>\n')


def _render_modules_only_in_b(out: StringIO, diff: ReportDiff) -> None:
    if not diff.modules_only_in_b:
        return
    out.write('<section class="diff-section added">\n')
    out.write(f"<h2>🆕 New trackers in {_h(diff.label_b)!s}</h2>\n")
    out.write('<ul class="diff-list">\n')
    for m in diff.modules_only_in_b:
        vendor = m.vendor_meta.vendor or ""
        juris = m.vendor_meta.legal_jurisdiction or ""
        juris_part = f" · {_h(juris)}" if juris else ""
        vendor_part = f' <span class="vendor">({_h(vendor)}{juris_part})</span>' if vendor else ""
        out.write(
            f'<li class="added"><strong>{_h(m.module_name)}</strong>'
            f"{vendor_part}</li>\n"
        )
    out.write("</ul>\n</section>\n")


def _render_modules_only_in_a(out: StringIO, diff: ReportDiff) -> None:
    if not diff.modules_only_in_a:
        return
    out.write('<section class="diff-section removed">\n')
    out.write(f"<h2>❌ Trackers no longer firing in {_h(diff.label_b)!s}</h2>\n")
    out.write('<ul class="diff-list">\n')
    for m in diff.modules_only_in_a:
        vendor = m.vendor_meta.vendor or ""
        vendor_part = f' <span class="vendor">({_h(vendor)})</span>' if vendor else ""
        out.write(
            f'<li class="removed"><strong>{_h(m.module_name)}</strong>'
            f"{vendor_part}</li>\n"
        )
    out.write("</ul>\n</section>\n")


def _render_modules_changed(out: StringIO, diff: ReportDiff) -> None:
    if not diff.modules_changed:
        return
    out.write('<section class="diff-section changed">\n')
    out.write("<h2>🔁 Trackers with field-set changes</h2>\n")
    out.write('<ul class="diff-list">\n')
    for c in diff.modules_changed:
        deltas: list[str] = []
        if c.fields_added:
            fields = ", ".join(_h(f) for f in c.fields_added)
            deltas.append(f'<span class="field-added">+{fields}</span>')
        if c.fields_removed:
            fields = ", ".join(_h(f) for f in c.fields_removed)
            deltas.append(f'<span class="field-removed">−{fields}</span>')
        if c.hit_count_a != c.hit_count_b:
            deltas.append(f"hits {c.hit_count_a} → {c.hit_count_b}")
        delta_str = " · ".join(deltas) if deltas else ""
        vendor_part = (
            f' <span class="vendor">({_h(c.vendor)})</span>' if c.vendor else ""
        )
        out.write(
            f'<li class="changed"><strong>{_h(c.module_name)}</strong>'
            f"{vendor_part}"
            f'<span class="delta">{delta_str}</span></li>\n'
        )
    out.write("</ul>\n</section>\n")


def _render_host_delta(out: StringIO, diff: ReportDiff) -> None:
    if not diff.hosts_only_in_a and not diff.hosts_only_in_b:
        return
    out.write('<section class="diff-section">\n')
    out.write("<h2>Third-party host delta</h2>\n")
    out.write('<table class="diff-hosts">\n')
    out.write(
        f"<thead><tr><th>Only in {_h(diff.label_a)}</th>"
        f"<th>Only in {_h(diff.label_b)}</th></tr></thead>\n"
    )
    out.write("<tbody>\n")
    rows = max(len(diff.hosts_only_in_a), len(diff.hosts_only_in_b))
    for i in range(rows):
        a_cell = _h(diff.hosts_only_in_a[i]) if i < len(diff.hosts_only_in_a) else ""
        b_cell = _h(diff.hosts_only_in_b[i]) if i < len(diff.hosts_only_in_b) else ""
        out.write(f"<tr><td><code>{a_cell}</code></td>"
                  f"<td><code>{b_cell}</code></td></tr>\n")
    out.write("</tbody></table>\n</section>\n")


def _render_finding_delta(out: StringIO, diff: ReportDiff) -> None:
    if not diff.findings_only_in_a and not diff.findings_only_in_b:
        return
    out.write('<section class="diff-section">\n')
    out.write("<h2>Finding delta</h2>\n")
    if diff.findings_only_in_b:
        out.write(f"<h3>New in {_h(diff.label_b)}</h3>\n<ul>\n")
        for f in diff.findings_only_in_b:
            out.write(
                f"<li>{f.badge} <strong>{_h(f.headline)}</strong>"
                + (f" — {_h(f.detail)}" if f.detail else "")
                + "</li>\n"
            )
        out.write("</ul>\n")
    if diff.findings_only_in_a:
        out.write(f"<h3>Gone in {_h(diff.label_b)}</h3>\n<ul>\n")
        for f in diff.findings_only_in_a:
            out.write(
                f"<li>{f.badge} <strong>{_h(f.headline)}</strong>"
                + (f" — {_h(f.detail)}" if f.detail else "")
                + "</li>\n"
            )
        out.write("</ul>\n")
    out.write("</section>\n")


def _render_new_jurisdictions(out: StringIO, diff: ReportDiff) -> None:
    if not diff.new_jurisdictions:
        return
    out.write('<section class="diff-section">\n')
    out.write(
        f"<h2>New jurisdictions appearing in {_h(diff.label_b)}</h2>\n"
    )
    out.write(
        "<p>"
        + ", ".join(f"<code>{_h(j)}</code>" for j in diff.new_jurisdictions)
        + "</p>\n"
    )
    out.write("</section>\n")


def _render_bundle_mismatch(out: StringIO, diff: ReportDiff) -> None:
    """Warning banner when A and B don't appear to be the same site."""
    if not diff.bundle_mismatch:
        return
    out.write(
        '<div class="capture-status failure">\n'
        f"<strong>⚠ Bundle mismatch.</strong> {_h(diff.bundle_mismatch)}\n"
        "</div>\n"
    )


def _render_side_report_links_html(
    out: StringIO, diff: ReportDiff,
    side_report_a: str | None, side_report_b: str | None,
) -> None:
    """Links to the per-side single-site reports when ``--out`` produced them."""
    if not side_report_a and not side_report_b:
        return
    out.write('<aside class="side-reports">\n')
    out.write("<strong>Full per-side reports:</strong>\n<ul>\n")
    if side_report_a:
        out.write(
            f'<li><a href="{_h(side_report_a)}">'
            f"{_h(diff.label_a)} — single-site report</a></li>\n"
        )
    if side_report_b:
        out.write(
            f'<li><a href="{_h(side_report_b)}">'
            f"{_h(diff.label_b)} — single-site report</a></li>\n"
        )
    out.write("</ul>\n</aside>\n")


def _render_personal_data_delta(out: StringIO, diff: ReportDiff) -> None:
    """Distinct PII/identifier fields that start or stop leaking between A→B."""
    pdd = diff.personal_data_delta
    if not pdd.only_in_a and not pdd.only_in_b:
        return
    out.write('<section class="diff-section added">\n')
    out.write(
        f"<h2>Personal-data field delta "
        f"({_h(diff.label_a)} {pdd.count_a} → {_h(diff.label_b)} {pdd.count_b})</h2>\n"
    )
    if pdd.only_in_b:
        out.write(f"<h3>New in <code>{_h(diff.label_b)}</code></h3>\n")
        out.write('<ul class="diff-list">\n')
        for f in pdd.only_in_b:
            out.write(
                f'<li class="added"><b>{_h(f.vendor)}</b>'
                f' <span class="vendor">({_h(f.category)})</span>'
                f' <code>{_h(f.key)}</code></li>\n'
            )
        out.write("</ul>\n")
    if pdd.only_in_a:
        out.write(f"<h3>Only in <code>{_h(diff.label_a)}</code></h3>\n")
        out.write('<ul class="diff-list">\n')
        for f in pdd.only_in_a:
            out.write(
                f'<li class="removed"><b>{_h(f.vendor)}</b>'
                f' <span class="vendor">({_h(f.category)})</span>'
                f' <code>{_h(f.key)}</code></li>\n'
            )
        out.write("</ul>\n")
    out.write("</section>\n")


def _render_cookie_delta(out: StringIO, diff: ReportDiff) -> None:
    """Cookies present in one capture but not the other (matched by name+host)."""
    if not diff.cookies_added and not diff.cookies_removed:
        return
    out.write('<section class="diff-section">\n')
    out.write("<h2>Cookie delta</h2>\n")
    if diff.cookies_added:
        out.write(f"<h3>New in <code>{_h(diff.label_b)}</code></h3>\n")
        out.write('<ul class="diff-list">\n')
        for c in diff.cookies_added:
            party = "1P" if c.is_first_party else "3P"
            out.write(
                f'<li class="added"><code>{_h(c.name)}</code>'
                f' <span class="vendor">@ {_h(c.host)}</span>'
                f' [{party}, {_h(c.lifetime_human)}]</li>\n'
            )
        out.write("</ul>\n")
    if diff.cookies_removed:
        out.write(f"<h3>Only in <code>{_h(diff.label_a)}</code></h3>\n")
        out.write('<ul class="diff-list">\n')
        for c in diff.cookies_removed:
            party = "1P" if c.is_first_party else "3P"
            out.write(
                f'<li class="removed"><code>{_h(c.name)}</code>'
                f' <span class="vendor">@ {_h(c.host)}</span>'
                f' [{party}, {_h(c.lifetime_human)}]</li>\n'
            )
        out.write("</ul>\n")
    out.write("</section>\n")


def _render_storage_delta(out: StringIO, diff: ReportDiff) -> None:
    """``localStorage``/``sessionStorage`` keys present on one side only."""
    if not diff.storage_added and not diff.storage_removed:
        return
    out.write('<section class="diff-section">\n')
    out.write("<h2>Browser-storage delta</h2>\n")
    if diff.storage_added:
        out.write(f"<h3>New in <code>{_h(diff.label_b)}</code></h3>\n")
        out.write('<ul class="diff-list">\n')
        for s in diff.storage_added:
            out.write(
                f'<li class="added">[{_h(s.kind)}] <code>{_h(s.key)}</code>'
                f' <span class="vendor">@ {_h(s.origin)}</span></li>\n'
            )
        out.write("</ul>\n")
    if diff.storage_removed:
        out.write(f"<h3>Only in <code>{_h(diff.label_a)}</code></h3>\n")
        out.write('<ul class="diff-list">\n')
        for s in diff.storage_removed:
            out.write(
                f'<li class="removed">[{_h(s.kind)}] <code>{_h(s.key)}</code>'
                f' <span class="vendor">@ {_h(s.origin)}</span></li>\n'
            )
        out.write("</ul>\n")
    out.write("</section>\n")


# --- Markdown --------------------------------------------------------------


def render_diff_markdown(
    diff: ReportDiff,
    *,
    side_report_a: str | None = None,
    side_report_b: str | None = None,
) -> str:
    """Render a :class:`ReportDiff` as a Markdown document.

    Mirrors the HTML structure (header, intro, headline, delta sections)
    using plain markdown — no inline HTML except links. Suitable for
    git-friendly artifacts, pandoc pipelines, and GitHub previews.

    ``side_report_a`` / ``side_report_b`` are optional relative paths
    to per-side reports. When set, "Full per-side reports" links appear
    near the top of the diff.
    """
    from .markdown import _render_capture_status_banner as md_capture_banner
    from .markdown import _render_intro as md_intro

    out = StringIO()
    host_label = diff.manifest_b.base_domain or diff.manifest_a.base_domain or "(diff)"
    out.write(
        f"# {BRANDING_TITLE_PREFIX} — Diff: {host_label}\n\n"
    )
    out.write(f"comparing `{diff.label_a}` → `{diff.label_b}`\n\n")

    md_intro(out)
    if diff.bundle_mismatch:
        out.write(f"> ⚠ **Bundle mismatch.** {diff.bundle_mismatch}\n\n")
    md_capture_banner(out, diff.capture_status_a)
    md_capture_banner(out, diff.capture_status_b)

    if side_report_a or side_report_b:
        out.write("**Full per-side reports:**\n\n")
        if side_report_a:
            out.write(
                f"- [`{diff.label_a}` — single-site report]({side_report_a})\n"
            )
        if side_report_b:
            out.write(
                f"- [`{diff.label_b}` — single-site report]({side_report_b})\n"
            )
        out.write("\n")

    out.write(f"> **{diff.headline}**\n\n")

    pdd = diff.personal_data_delta
    if pdd.only_in_a or pdd.only_in_b:
        out.write(
            f"## Personal-data field delta "
            f"(`{diff.label_a}` {pdd.count_a} → `{diff.label_b}` {pdd.count_b})\n\n"
        )
        if pdd.only_in_b:
            out.write(f"### New in `{diff.label_b}`\n\n")
            for f in pdd.only_in_b:
                out.write(f"- **{f.vendor}** ({f.category}) `{f.key}`\n")
            out.write("\n")
        if pdd.only_in_a:
            out.write(f"### Only in `{diff.label_a}`\n\n")
            for f in pdd.only_in_a:
                out.write(f"- **{f.vendor}** ({f.category}) `{f.key}`\n")
            out.write("\n")

    if diff.modules_only_in_b:
        out.write(f"## 🆕 New trackers in `{diff.label_b}`\n\n")
        for m in diff.modules_only_in_b:
            vendor = m.vendor_meta.vendor or ""
            juris = m.vendor_meta.legal_jurisdiction or ""
            extras = []
            if vendor:
                extras.append(vendor)
            if juris:
                extras.append(juris)
            tail = f" *({' · '.join(extras)})*" if extras else ""
            out.write(f"- **{m.module_name}**{tail}\n")
        out.write("\n")

    if diff.modules_changed:
        out.write("## 🔁 Trackers with field-set changes\n\n")
        for c in diff.modules_changed:
            vendor = f" *({c.vendor})*" if c.vendor else ""
            out.write(f"- **{c.module_name}**{vendor}\n")
            if c.fields_added:
                out.write(f"  - `+{', '.join(c.fields_added)}`\n")
            if c.fields_removed:
                out.write(f"  - `−{', '.join(c.fields_removed)}`\n")
            if c.hit_count_a != c.hit_count_b:
                out.write(f"  - hits: {c.hit_count_a} → {c.hit_count_b}\n")
        out.write("\n")

    if diff.modules_only_in_a:
        out.write(f"## ❌ Trackers no longer firing in `{diff.label_b}`\n\n")
        for m in diff.modules_only_in_a:
            vendor = f" *({m.vendor_meta.vendor})*" if m.vendor_meta.vendor else ""
            out.write(f"- **{m.module_name}**{vendor}\n")
        out.write("\n")

    if diff.cookies_added or diff.cookies_removed:
        out.write("## Cookie delta\n\n")
        if diff.cookies_added:
            out.write(f"### New in `{diff.label_b}`\n\n")
            for c in diff.cookies_added:
                party = "1P" if c.is_first_party else "3P"
                out.write(
                    f"- `{c.name}` @ `{c.host}` "
                    f"[{party}, {c.lifetime_human}]\n"
                )
            out.write("\n")
        if diff.cookies_removed:
            out.write(f"### Only in `{diff.label_a}`\n\n")
            for c in diff.cookies_removed:
                party = "1P" if c.is_first_party else "3P"
                out.write(
                    f"- `{c.name}` @ `{c.host}` "
                    f"[{party}, {c.lifetime_human}]\n"
                )
            out.write("\n")

    if diff.storage_added or diff.storage_removed:
        out.write("## Browser-storage delta\n\n")
        if diff.storage_added:
            out.write(f"### New in `{diff.label_b}`\n\n")
            for s in diff.storage_added:
                out.write(f"- [{s.kind}] `{s.key}` @ `{s.origin}`\n")
            out.write("\n")
        if diff.storage_removed:
            out.write(f"### Only in `{diff.label_a}`\n\n")
            for s in diff.storage_removed:
                out.write(f"- [{s.kind}] `{s.key}` @ `{s.origin}`\n")
            out.write("\n")

    if diff.hosts_only_in_a or diff.hosts_only_in_b:
        out.write("## Third-party host delta\n\n")
        if diff.hosts_only_in_b:
            out.write(f"### Only in `{diff.label_b}`\n\n")
            for h in diff.hosts_only_in_b:
                out.write(f"- `{h}`\n")
            out.write("\n")
        if diff.hosts_only_in_a:
            out.write(f"### Only in `{diff.label_a}`\n\n")
            for h in diff.hosts_only_in_a:
                out.write(f"- `{h}`\n")
            out.write("\n")

    if diff.findings_only_in_a or diff.findings_only_in_b:
        out.write("## Finding delta\n\n")
        if diff.findings_only_in_b:
            out.write(f"### New in `{diff.label_b}`\n\n")
            for f in diff.findings_only_in_b:
                detail = f" — {f.detail}" if f.detail else ""
                out.write(f"- {f.badge} **{f.headline}**{detail}\n")
            out.write("\n")
        if diff.findings_only_in_a:
            out.write(f"### Gone in `{diff.label_b}`\n\n")
            for f in diff.findings_only_in_a:
                detail = f" — {f.detail}" if f.detail else ""
                out.write(f"- {f.badge} **{f.headline}**{detail}\n")
            out.write("\n")

    if diff.new_jurisdictions:
        out.write(f"## New jurisdictions in `{diff.label_b}`\n\n")
        out.write(", ".join(f"`{j}`" for j in diff.new_jurisdictions) + "\n\n")

    return out.getvalue()


